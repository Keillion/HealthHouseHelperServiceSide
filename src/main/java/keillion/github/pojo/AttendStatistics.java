package keillion.github.pojo;

public class AttendStatistics {
    private Long id;

    private Integer year;

    private Integer quarter;

    private Integer month;

    private Integer date;

    private Integer week;

    private Integer day;

    private Integer house;

    private Long numofattend;

    private Long numofpeople;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Integer getQuarter() {
        return quarter;
    }

    public void setQuarter(Integer quarter) {
        this.quarter = quarter;
    }

    public Integer getMonth() {
        return month;
    }

    public void setMonth(Integer month) {
        this.month = month;
    }

    public Integer getDate() {
        return date;
    }

    public void setDate(Integer date) {
        this.date = date;
    }

    public Integer getWeek() {
        return week;
    }

    public void setWeek(Integer week) {
        this.week = week;
    }

    public Integer getDay() {
        return day;
    }

    public void setDay(Integer day) {
        this.day = day;
    }

    public Integer getHouse() {
        return house;
    }

    public void setHouse(Integer house) {
        this.house = house;
    }

    public Long getNumofattend() {
        return numofattend;
    }

    public void setNumofattend(Long numofattend) {
        this.numofattend = numofattend;
    }

    public Long getNumofpeople() {
        return numofpeople;
    }

    public void setNumofpeople(Long numofpeople) {
        this.numofpeople = numofpeople;
    }
}